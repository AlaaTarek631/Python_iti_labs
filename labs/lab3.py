#taking numbers from users
num1 = int(input("enter first number"))
num2 = int(input("enter second number"))

#bitwise operator

print (num1&num2)
print (num1|num2)
print (num1^num2)
print (~num2)
print (num2<<1)
print (num2>>1)

#Mathematical Operation

print (num1+num2)
print (num1-num2)
print (num1*num2)
print (num1/num2)
print (num1//num2)
print (num1**num2)

#Relational Operation
print (num1==num2)
print (num1>num2)
print (num1<num2)
